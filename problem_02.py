# Write a program to check char is vowel or not.

# SOLUTION:

char = input("Enter any character:").lower()
if char == "a":
    print("vowel")
elif char == "e":
    print("vowel")
elif char == "i":
    print("vowel")
elif char == "o":
    print("vowel")
elif char == "u":
    print("vowel")
else:
    print("Character is not a vowel")